    /// 打卡回覆中的信念轉化引導
    static func checkInBeliefPrompt(partnerRole: AIPartnerRole, checkInContent: String, dayNumber: Int, totalDays: Int) -> String {
        let roleContext: String
        switch partnerRole {
        case .fellowStarter:
            roleContext = "你是同行者，也在同時進行這個挑戰。用共情的方式回應。"
        case .experiencedGuide:
            roleContext = "你是過來人，已完成過類似挑戰。用過來人的經驗回應。"
        case .inspiredBeginner:
            roleContext = "你是新手，被用戶的行動激勵。用好奇和敬佩的方式回應。"
        case .coach:
            roleContext = "你是教練，適時點撥。用引導性問題回應。"
        }

        return """
        \(roleContext)

        用戶打卡了：「\(checkInContent)」（第\(dayNumber)天/共\(totalDays)天）

        在你的回覆中（30-80字）：
        1. 先肯定用戶的行動（行動本身就是最好的信念反駁）
        2. 如果打卡內容暗示了猶豫/懷疑，溫和地引導一個開放性視角
        3. 如果打卡內容很積極，強化這個新信念：「你的行動正在改寫那個舊故事」
        4. 保持角色語氣一致性
        """
    }
