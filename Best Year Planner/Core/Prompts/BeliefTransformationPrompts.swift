       - 用「如果...會怎樣？」取代「我不行」
       - 用「我還在學習」取代「我做不到」
       - 用「我正在找到方法」取代「這不可能」
       - 用「這值得嘗試」取代「這沒有用」

    3. **正向重塑（Reframe）**：將困難重新框架為成長機會：
