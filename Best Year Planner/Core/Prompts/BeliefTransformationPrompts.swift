    /// 每日 AI Tip prompt（含信念轉化引導）
    static func dailyTipPrompt(dayNumber: Int, completedCount: Int, totalDays: Int, challengeType: String) -> String {
        let beliefContext: String
        let progress = Double(dayNumber) / Double(totalDays)

        if progress < 0.3 {
            beliefContext = """
            用戶處於初期階段，最容易出現的限制性信念：
            - 「我可能撐不下去」→ 引導：你已經堅持了\(completedCount)天，這本身就是證明
            - 「別人做得比我好」→ 引導：比較是舊信念，今天的你比昨天的你進步了什麼？
            - 「這有什麼用」→ 引導：每次小行動都在重新定義你是誰
            """
        } else if progress < 0.7 {
            beliefContext = """
            用戶處於中期階段，容易出現的限制性信念：
            - 「我好像沒什麼進步」→ 引導：進步不是線性的，低谷是突破的前兆
            - 「算了，這樣就好」→ 引導：這不是滿足，是恐懼在裝冷靜。再多走一步呢？
            - 「別人早就放棄了」→ 引導：你沒有放棄，這本身就是反常規的勇氣
            """
        } else {
            beliefContext = """
            用戶處於後期階段，需要鞏固新信念：
            - 「快要結束了，之後呢」→ 引導：不是結束，是你已經成為那個會持續行動的人
            - 「偶爾休息一下沒關係」→ 引導：休息不是退回原點，你已經不是原來的你了
            - 「我不確定這能持續」→ 引導：21天的行動已經改寫了你的自我敘事
            """
        }

        return """
        用戶正在進行\(challengeType)第\(dayNumber)天，已連續完成\(completedCount)天。

        \(beliefContext)

        請給一句簡短鼓勵（30字以內），必須包含信念轉化元素：
        - 溫和地挑戰一個可能的限制性信念，或
        - 強化用戶已經證明的新信念，或
        - 用「如果...會怎樣」引導開放性思考

        不要空泛的「加油」，要有具體的信念轉化力量。
        """
    }

